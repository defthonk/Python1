#Concatenar y usar el mismo ejemmplo de arriba (texto, nombre y year)
Texto = "repaso"
Nombre = "Juanes"
Year = 2022

print(f"{Texto}-{Nombre}-{str(Year)}")

#La respuesta de esto seria (repaso-Juanes-2022) muestra todo en una linea y se usa str para convertir el numero en un string 


#Otra forma de concatenar

print(Texto + " - " + Nombre + " - " + str(Year))

